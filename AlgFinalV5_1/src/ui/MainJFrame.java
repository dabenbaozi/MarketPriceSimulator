    private void jBtnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnStartActionPerformed

        //price range: 0-x && max>min
        //qty range:0-x && max> min
        //thread number: 30-50
        //elasticity:Coefficient (E); elasticity of demand	0-x
        //Relatively Elastic	1 < E < âˆž
        //Unit Elastic              E = 1
        //Relatively Inelastic	0 < E < 1
        //Perfectly Inelastic	E = 0
        /////////////////////collection to store results//////////////////////////////////////////////////
        ////////////////////Validation starts//////////////////////////////////////////////////////////////                   
        if ("".equals(txtThreadNumber.getText())) {
            JOptionPane.showMessageDialog(null, "Plese enter how many Threads of Swarm do you want to draw", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (Integer.valueOf(txtThreadNumber.getText()) < 30 || Integer.valueOf(txtThreadNumber.getText()) > 50) {
            JOptionPane.showMessageDialog(null, "The number of Thread has to between the range of 30 to 50", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if ("".equals(txtPriceMin.getText()) || "".equals(txtPriceMax.getText())) {
            JOptionPane.showMessageDialog(null, "Plese enter the price range for your product", "Warning", JOptionPane.WARNING_MESSAGE);
        } 
        else if (Double.valueOf(txtPriceMin.getText()) >50 || Double.valueOf(txtPriceMin.getText())< 20){
            JOptionPane.showMessageDialog(null, "Plese enter a valid price range for your product", "Warning", JOptionPane.WARNING_MESSAGE);
        } 
        else if (Double.valueOf(txtPriceMin.getText()) > Double.valueOf(txtPriceMax.getText()) || Double.valueOf(txtPriceMin.getText()) < 0) {
            JOptionPane.showMessageDialog(null, "Plese enter a valid price range for your product", "Warning", JOptionPane.WARNING_MESSAGE);
        } //        else if (Double.valueOf(txtRevMin.getText())>Double.valueOf(txtRevMax.getText())||Double.valueOf(txtPriceMin.getText())<0){
        //            JOptionPane.showMessageDialog(null,"Plese enter a valid price range for your product","Warning",JOptionPane.WARNING_MESSAGE);
        //        }
        else if (Double.valueOf(txtElasticity.getText()).equals("") || Double.valueOf(txtElasticity.getText()) < 0) {
            JOptionPane.showMessageDialog(null, "Plese enter a valid elasticity for your product", "Warning", JOptionPane.WARNING_MESSAGE);
        } //done with validation and start with creating threads
        else {
            int threadNumber = Integer.valueOf(txtThreadNumber.getText());
            double priceMin = Double.valueOf(txtPriceMin.getText());
            double priceMax = Double.valueOf(txtPriceMax.getText());
            double estimateRev = Double.valueOf(txtRevMin.getText());
            elasticity = Double.valueOf(txtElasticity.getText());

            MyCanvas canvas = new MyCanvas();
            canvas.setSize(1024,1024);
            splitJPanel.setRightComponent(canvas);
            
             //int SWARM_SIZE,double estimateRev, double LOC_Y_LOW,double LOC_Y_HIGH
                Swarm swarm = new Swarm(threadNumber,estimateRev,priceMin,priceMax);
               
                //give value to resultSet so other classes could know value
                for(int i = 0; i<swarm.getMyRunnable().size();i++){
                resultSet.add(swarm.getMyRunnable().get(i));
                }
                //init thread
                for(int x = 0; x <swarm.getMyRunnable().size();x++){
                    Agent a = swarm.getMyRunnable().get(x);
                    a.run();
                    
                    System.out.println("thread start" + a.getAgentID());
                }
     

        }


    }//GEN-LAST:event_jBtnStartActionPerformed

